def _storage_module(name: str) -> str:
    class_name = _title(name).replace(" ", "") + "StorageBackend"
    return f'''from __future__ import annotations

from typing import Any

from openmiura.extensions.sdk import ExtensionLifecycleContext, ExtensionManifest, StorageContext


class {class_name}:
    manifest = ExtensionManifest(
        name="{name}",
        kind="storage_backend",
        version="0.1.0",
        description="Starter storage backend scaffold for {name}",
        compatibility={{"min_openmiura_version": "1.0.0", "tested_contract_versions": ["1.0"]}},
    )

    def initialize(self, ctx: ExtensionLifecycleContext) -> None:
        self._ctx = ctx
        self._store: dict[str, Any] = {{}}

    def _scoped_key(self, key: str, ctx: StorageContext) -> str:
        tenant = ctx.tenant_id or "global"
        workspace = ctx.workspace_id or "default"
        environment = ctx.environment or "default"
        return f"{{tenant}}:{{workspace}}:{{environment}}:{{key}}"

    def get(self, key: str, ctx: StorageContext) -> Any:
        return self._store.get(self._scoped_key(key, ctx))

    def put(self, key: str, value: Any, ctx: StorageContext) -> None:
        self._store[self._scoped_key(key, ctx)] = value

    def delete(self, key: str, ctx: StorageContext) -> None:
        self._store.pop(self._scoped_key(key, ctx), None)

    def shutdown(self) -> None:
        self._ctx = None
        self._store = {{}}


storage_backend = {class_name}()
'''


def _smoke_test(kind: str) -> str:
    return f'''from __future__ import annotations

from pathlib import Path

from openmiura.extensions.sdk.harness import ExtensionHarness



def test_extension_smoke() -> None:
    report = ExtensionHarness().run(Path(__file__).resolve().parents[1])
    assert report.ok is True
    assert report.manifest is not None
    assert report.manifest.kind == "{kind}"
    assert report.smoke_result is not None
'''



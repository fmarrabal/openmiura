def _channel_module(name: str) -> str:
    class_name = _title(name).replace(" ", "") + "Channel"
    return f'''from __future__ import annotations

from typing import Any

from openmiura.extensions.sdk import ChannelAdapterContext, ChannelMessage, ExtensionLifecycleContext, ExtensionManifest


class {class_name}:
    manifest = ExtensionManifest(
        name="{name}",
        kind="channel_adapter",
        version="0.1.0",
        description="Starter channel adapter scaffold for {name}",
        compatibility={{"min_openmiura_version": "1.0.0", "tested_contract_versions": ["1.0"]}},
    )

    def initialize(self, ctx: ExtensionLifecycleContext) -> None:
        self._ctx = ctx

    def normalize_inbound(self, payload: Any, ctx: ChannelAdapterContext) -> ChannelMessage:
        text = payload.get("text", "") if isinstance(payload, dict) else str(payload)
        channel_user_id = payload.get("user_id", "demo-user") if isinstance(payload, dict) else "demo-user"
        return ChannelMessage(channel=self.manifest.name, channel_user_id=channel_user_id, text=text)

    def format_outbound(self, response: Any, ctx: ChannelAdapterContext) -> Any:
        return {{"channel": self.manifest.name, "response": response}}

    def shutdown(self) -> None:
        self._ctx = None


channel = {class_name}()
'''



from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
import re

_KIND_ALIASES = {
    "tool": "tool",
    "skill": "skill",
    "provider": "llm_provider",
    "llm_provider": "llm_provider",
    "channel": "channel_adapter",
    "channel_adapter": "channel_adapter",
    "auth": "auth_provider",
    "auth_provider": "auth_provider",
    "storage": "storage_backend",
    "storage_backend": "storage_backend",
    "workflow": "workflow",
}


@dataclass(slots=True)
class ScaffoldResult:
    kind: str
    name: str
    root: Path
    created_files: list[Path]

    def to_dict(self) -> dict[str, object]:
        return {
            "kind": self.kind,
            "name": self.name,
            "root": str(self.root),
            "created_files": [str(path) for path in self.created_files],
        }


def _slugify(value: str) -> str:
    text = re.sub(r"[^a-zA-Z0-9]+", "-", str(value or "").strip()).strip("-").lower()
    if not text:
        raise ValueError("Name cannot be empty")
    return text


def _module_name(value: str) -> str:
    slug = _slugify(value).replace("-", "_")
    if slug and slug[0].isdigit():
        slug = f"ext_{slug}"
    return slug


def _title(value: str) -> str:
    return _slugify(value).replace("-", " ").title()


def _write(path: Path, content: str) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.rstrip() + "\n", encoding="utf-8")
    return path


def _manifest(kind: str, name: str, module_name: str, entry_symbol: str, author: str) -> str:
    entrypoint = f"{module_name}:{entry_symbol}" if entry_symbol else ""
    lines = [
        'manifest_version: "1"',
        'contract_version: "1.0"',
        f"name: {name}",
        f"kind: {kind}",
        "version: 0.1.0",
        f"author: {author}",
        f"description: Starter {kind} scaffold for {name}",
        "enabled: true",
        "tags:",
        "  - starter",
        "permissions: []",
        "capabilities: []",
    ]
    if entrypoint:
        lines.append(f"entrypoint: {entrypoint}")
    lines.extend(
        [
            "config_schema:",
            "  type: object",
            "  additionalProperties: false",
            "  properties: {}",
            "metadata: {}",
            "compatibility:",
            '  min_openmiura_version: "1.0.0"',
            "  max_openmiura_version: null",
            "  tested_contract_versions:",
            '    - "1.0"',
            "review:",
            "  reviewers: []",
            "  required_approvals: 1",
        ]
    )
    return "\n".join(lines)


def _package_pyproject(dist_name: str, module_name: str) -> str:
    return f'''[build-system]
requires = ["setuptools>=68", "wheel"]
build-backend = "setuptools.build_meta"

[project]
name = "{dist_name}"
version = "0.1.0"
description = "Extension scaffold for openMiura"
readme = "README.md"
requires-python = ">=3.10"
dependencies = ["openmiura"]

[tool.setuptools]
py-modules = ["{module_name}"]
'''


def _readme(kind: str, name: str) -> str:
    title = _title(name)
    return f'''# {title}

Starter scaffold for an openMiura {kind}.

## Files
- `manifest.yaml`: public extension contract
- `pyproject.toml`: packaging metadata
- implementation module
- `tests/test_smoke.py`: local smoke test
- `CHANGELOG.md`: release notes for the registry flow

## Local checks
```bash
openmiura sdk quickstart --kind {kind}
openmiura sdk validate-manifest manifest.yaml
openmiura sdk test-extension .
openmiura registry publish . --root ./registry --namespace demo
openmiura registry review-start {name} 0.1.0 --root ./registry --namespace demo
openmiura registry approve {name} 0.1.0 --root ./registry --namespace demo
openmiura registry verify {name} 0.1.0 --root ./registry --namespace demo
openmiura registry policy-set demo --root ./registry --allowed-namespace demo --allowed-kind {kind}
openmiura registry install {name} --root ./registry --namespace demo --tenant demo
```
'''


def _changelog(name: str) -> str:
    return f'''# Changelog

## 0.1.0
- Initial release of `{name}`.
- Generated by openMiura scaffolding.
'''


def _tool_module(name: str) -> str:
    class_name = _title(name).replace(" ", "") + "Tool"
    return f'''from __future__ import annotations

from typing import Any

from openmiura.extensions.sdk import ExtensionLifecycleContext, ExtensionManifest, ToolExecutionContext


class {class_name}:
    manifest = ExtensionManifest(
        name="{name}",
        kind="tool",
        version="0.1.0",
        description="Starter tool scaffold for {name}",
        compatibility={{"min_openmiura_version": "1.0.0", "tested_contract_versions": ["1.0"]}},
    )

    def initialize(self, ctx: ExtensionLifecycleContext) -> None:
        self._ctx = ctx

    def execute(self, arguments: dict[str, Any], ctx: ToolExecutionContext) -> Any:
        return {{
            "ok": True,
            "tool": self.manifest.name,
            "agent_name": ctx.agent_name,
            "arguments": dict(arguments),
        }}

    def shutdown(self) -> None:
        self._ctx = None


tool = {class_name}()
'''


def _skill_module(name: str) -> str:
    class_name = _title(name).replace(" ", "") + "Skill"
    return f'''from __future__ import annotations

from typing import Any

from openmiura.extensions.sdk import ExtensionLifecycleContext, ExtensionManifest, SkillExecutionContext


class {class_name}:
    manifest = ExtensionManifest(
        name="{name}",
        kind="skill",
        version="0.1.0",
        description="Starter skill scaffold for {name}",
        compatibility={{"min_openmiura_version": "1.0.0", "tested_contract_versions": ["1.0"]}},
    )

    def initialize(self, ctx: ExtensionLifecycleContext) -> None:
        self._ctx = ctx

    def extend_agent_config(self, agent_cfg: dict[str, Any], ctx: SkillExecutionContext) -> dict[str, Any]:
        updated = dict(agent_cfg)
        skills = list(updated.get("skills") or [])
        if self.manifest.name not in skills:
            skills.append(self.manifest.name)
        updated["skills"] = skills
        updated.setdefault("metadata", {{}})
        updated["metadata"]["extended_by"] = self.manifest.name
        return updated

    def shutdown(self) -> None:
        self._ctx = None


skill = {class_name}()
'''


